Scaife Sports Centre.</p>
<p>
Subscription to the Pocklington Squash Club is &pound;20 per annum for adults and &pound;10 for juniors,
 membership forms are available from the Squash Club noticeboard which is behind the squash courts at the Francis Scaife Sports Centre.
</p>
<p>
The club currently runs two mens teams in the <a href="http://yorkshiresquash.countyleagues.co.uk/">York & District Leagues</a>, and one womens team.  We also run mini-leagues every month, 
and a series of competitions at the end of the season designed for everyone to join in,  meet other players and have some fun playing
squash.  As well as this we run occasional club nights and social nights, details are on the notice board.
</p>
<h2>Squash 57 (Racketball)</h2>
<p>Squash 57 is a fast growing sport, and is ideal for those who want to play something a little 
kinder on the knees than squash! You can book the courts to play Racketball, and borrow rackets and 
a ball from the Francis Scaife reception.  </p>
<h2>Racket Restringing</h2>
<p>Matthew Dick offers a racket restringing service at the club. He restrings rackets using Ashaway Supernick string, the same string
used by the professionals. The service costs £20 per racket, and should take no more than a few days. You can find Matthew's contact 
details on the Members page.</p>

